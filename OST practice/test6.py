names = []
countries = []
matches = []
goals = []

for x in range(0,5):
    name = input("Enter player " + str(x+1)+ " name:")
    names.append(name)
    country = input("Enter player " + str(x+1)+ " country:")
    countries.append(country)
    match = input("Enter player " + str(x+1)+ " matches:")
    matches.append(match)
    goal = input("Enter player " + str(x+1)+ " goals:")
    goals.append(goal)


